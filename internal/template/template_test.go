// templatesDir points at the real templates directory relative to this test file.
const templatesDir = "../../templates"

func newTestEngine(t *testing.T) *Engine {
	t.Helper()
	fsys := os.DirFS(templatesDir)
	// Wrap in a sub-FS that mimics the "templates/" prefix the engine expects.
	// Since the engine prepends "templates/", we need to provide an FS rooted
	// one level above. Use os.DirFS at the frank-cli root instead.
	_ = fsys
	return New(os.DirFS("../.."))
}

func TestRenderRuntimeDockerfile_FrankenPHP(t *testing.T) {
	e := newTestEngine(t)
	out, err := e.RenderRuntime("frankenphp", "Dockerfile.tmpl", Data{PHPVersion: "8.4"})
	if err != nil {
		t.Fatalf("RenderRuntime error: %v", err)
	}
	if !strings.Contains(out, "dunglas/frankenphp:1-php8.4") {
		t.Errorf("expected PHP version in FROM line, got:\n%s", out)
	}
	if !strings.Contains(out, "# Generated by Frank") {
		t.Error("expected generated-by header")
	}
}

