func TestRenderServiceEnv_Pgsql(t *testing.T) {
	e := newTestEngine(t)
	out, err := e.RenderServiceEnv("pgsql", config.ServiceConfig{}, "my-project")
	if err != nil {
		t.Fatalf("RenderServiceEnv error: %v", err)
	}
	checks := []string{
		"DB_CONNECTION=pgsql",
		"DB_HOST=pgsql",
		"DB_PORT=5432",
		"DB_DATABASE=my-project",
		"DB_USERNAME=sail",
		"DB_PASSWORD=password",
	}
	for _, want := range checks {
		if !strings.Contains(out, want) {
			t.Errorf("expected %q in pgsql env, got:\n%s", want, out)
		}
	}
}

