package compose

import (
	"os"
	"strings"
	"testing"

	"github.com/phlisg/frank-cli/internal/config"
	"github.com/phlisg/frank-cli/internal/template"
)

func newTestGenerator(t *testing.T) *Generator {
	t.Helper()
	engine := template.New(os.DirFS("../.."))
	return New(engine)
}

func TestGenerate_FrankenPHPWithPgsql(t *testing.T) {
	g := newTestGenerator(t)
	cfg := &config.Config{
		PHP:      config.PHP{Version: "8.5", Runtime: "frankenphp"},
		Laravel:  config.Laravel{Version: "latest"},
		Services: []string{"pgsql", "mailpit"},
	}

	out, err := g.Generate(cfg, "myapp")
	if err != nil {
		t.Fatalf("Generate error: %v", err)
	}

	checks := []string{
		"# Generated by Frank",
		"laravel.test:",
		"pgsql:",
		"mailpit:",
		"networks:",
		"frank:",
		"driver: bridge",
		"pgsql_data:",
	}
	for _, want := range checks {
		if !strings.Contains(out, want) {
			t.Errorf("expected %q in output:\n%s", want, out)
		}
	}
}

func TestGenerate_FPMWithMySQL(t *testing.T) {
	g := newTestGenerator(t)
	cfg := &config.Config{
		PHP:      config.PHP{Version: "8.3", Runtime: "fpm"},
		Services: []string{"mysql", "redis"},
	}

	out, err := g.Generate(cfg, "testapp")
	if err != nil {
		t.Fatalf("Generate error: %v", err)
	}

	if !strings.Contains(out, "laravel.test:") {
		t.Error("expected laravel.test service")
	}
	if !strings.Contains(out, "nginx:") {
		t.Error("expected nginx service for fpm runtime")
	}
	if !strings.Contains(out, "mysql:") {
		t.Error("expected mysql service")
	}
	if !strings.Contains(out, "redis:") {
		t.Error("expected redis service")
	}
	if !strings.Contains(out, "mysql_data:") {
		t.Error("expected mysql_data volume")
	}
	if !strings.Contains(out, "redis_data:") {
		t.Error("expected redis_data volume")
	}
}

