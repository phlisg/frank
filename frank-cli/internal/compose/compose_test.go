func TestWrite_CreatesFile(t *testing.T) {
	g := newTestGenerator(t)
	cfg := config.New()
	dir := t.TempDir()

	if err := g.Write(cfg, "myapp", dir); err != nil {
		t.Fatalf("Write error: %v", err)
	}

	data, err := os.ReadFile(dir + "/compose.yaml")
	if err != nil {
		t.Fatalf("compose.yaml not created: %v", err)
	}
	if !strings.Contains(string(data), "laravel.test:") {
		t.Error("expected laravel.test in written compose.yaml")
	}
}

func TestHostPortKey(t *testing.T) {
	cases := []struct {
		in   string
		want string
	}{
		{"5432:5432", "5432/tcp"},
		{"443:443/udp", "443/udp"},
		{"80:80", "80/tcp"},
		{"1025:1025", "1025/tcp"},
	}
	for _, c := range cases {
		got := hostPortKey(c.in)
		if got != c.want {
			t.Errorf("hostPortKey(%q) = %q, want %q", c.in, got, c.want)
		}
	}
}

