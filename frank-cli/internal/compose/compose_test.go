func TestGenerate_NoVolumesWhenNoneNeeded(t *testing.T) {
	g := newTestGenerator(t)
	cfg := &config.Config{
		PHP:      config.PHP{Version: "8.5", Runtime: "frankenphp"},
		Services: []string{"mailpit", "memcached"},
	}

	out, err := g.Generate(cfg, "myapp")
	if err != nil {
		t.Fatalf("Generate error: %v", err)
	}
	// Neither mailpit nor memcached needs a named volume
	if strings.Contains(out, "mailpit_data") || strings.Contains(out, "memcached_data") {
		t.Error("unexpected volume entries for services that don't need them")
	}
}

